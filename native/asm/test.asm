INITUS:
	LEA AX, [@PREF]
	MOV CX, 12
	PUSH AX
	PUSH CX
	CALL @STRPRINT

	MOV AX, CS
	PUSH AX
	CALL @HEXPRINT

	LEA AX, [@COLON]
	MOV CX, 1
	PUSH AX
	PUSH CX
	CALL @STRPRINT

	LEA AX, [@INITUS]
	PUSH AX
	CALL @HEXPRINT

	LEA AX, [@CR]
	MOV CX, 2
	PUSH AX
	PUSH CX
	CALL @STRPRINT

	MOV AX, 0x4C00
	INT 0x21
PREF:	# DATA 1 'APP ORIGIN: '
COLON:	# DATA 1 ':'
CR:		# DATA 1 13 # DATA 1 10

HEXPRINT:
	PUSH BP
	MOV BP, SP
	PUSH AX
	PUSH CX
	PUSH DX
	PUSH BX
	MOV CX, [BP + 4]

	LEA BX, [@HEXNUM]
	MOV DX, CX
	SHR DX, 12
	ADD BX, DX
	MOV DL, [BX]
	MOV AH, 0x02
	INT 0x21
	
	LEA BX, [@HEXNUM]
	MOV DX, CX
	SHR DX, 8
	AND DX, 0xF
	ADD BX, DX
	MOV DL, [BX]
	MOV AH, 0x02
	INT 0x21

	LEA BX, [@HEXNUM]
	MOV DX, CX
	SHR DX, 4
	AND DX, 0xF
	ADD BX, DX
	MOV DL, [BX]
	MOV AH, 0x02
	INT 0x21

	LEA BX, [@HEXNUM]
	MOV DX, CX
	AND DX, 0xF
	ADD BX, DX
	MOV DL, [BX]
	MOV AH, 0x02
	INT 0x21

	POP BX
	POP DX
	POP CX
	POP AX
	POP BP
	RET 2
HEXNUM: # DATA 1 '0123456789ABCDEF'

STRPRINT:
	PUSH BP
	MOV BP, SP
	PUSH AX
	PUSH BX
	PUSH CX
	PUSH DX
	MOV CX, [BP + 4]
	MOV BX, [BP + 6]
	STRPRINT_LOOP:
		TEST CX, 0xFFFF
		JZ @STRPRINT_EXIT
		MOV DL, [BX]
		MOV AH, 0x02
		INT 0x21
		DEC CX
		INC BX
		JMP @STRPRINT_LOOP
	STRPRINT_EXIT:
	POP DX
	POP CX
	POP BX
	POP AX
	POP BP
	RET 2